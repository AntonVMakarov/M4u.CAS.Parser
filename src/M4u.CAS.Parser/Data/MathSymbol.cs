namespace M4u.CAS.Parser;

/// <summary>Статический класс для получения обозначений математических символов в виде строки.</summary>
internal static class MathSymbol
{
    /// <summary>Обозначение операции плюс</summary>
    internal static string Plus => "+";

    /// <summary>Обозначение операции умножения</summary>
    internal static string Times => "*";

    /// <summary>Обозначение операции вычитания</summary>
    internal static string Minus => "-";

    /// <summary>Обозначение операции деления</summary>
    internal static string Div => "/";
}
